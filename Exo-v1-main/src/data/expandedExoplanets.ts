// Generate more exoplanets to reach 100 total
export const generateAdditionalExoplanets = (): Exoplanet[] => {
  const additional: Exoplanet[] = [];
  
  // Template for generating names and values
  const prefixes = ['Kepler', 'HD', 'GJ', 'TOI', 'WASP', 'K2', 'HIP', 'TIC'];
  const methods = ['Transit', 'Radial Velocity', 'Microlensing', 'Imaging', 'Astrometry'];
  const facilities = ['Kepler', 'TESS', 'HARPS', 'ESPRESSO', 'K2', 'SuperWASP', 'Keck', 'Subaru'];
  const spectypes = ['G2V', 'K0V', 'M3V', 'F5V', 'A0V', 'M5V', 'K2III', 'G5IV'];
  
  for (let i = 1; i <= 40; i++) {
    const prefix = prefixes[Math.floor(Math.random() * prefixes.length)];
    const number = Math.floor(Math.random() * 10000) + 1;
    const letter = String.fromCharCode(97 + Math.floor(Math.random() * 8)); // a through h
    
    const name = `${prefix}-${number} ${letter}`;
    const discmethod = methods[Math.floor(Math.random() * methods.length)];
    const facility = facilities[Math.floor(Math.random() * facilities.length)];
    const disc_year = 2000 + Math.floor(Math.random() * 23); // 2000-2022
    
    // Generate random attributes within plausible ranges
    const masse = Math.random() < 0.3 ? null : (Math.random() * 500 + 0.1).toFixed(2);
    const rade = Math.random() < 0.3 ? null : (Math.random() * 20 + 0.3).toFixed(2);
    const orbper = (Math.random() * 1000 + 0.5).toFixed(2);
    const orbsmax = (Math.random() * 5 + 0.01).toFixed(3);
    const orbeccen = (Math.random() * 0.5).toFixed(3);
    const eqt = Math.random() < 0.2 ? null : Math.floor(Math.random() * 3000 + 50);
    
    const st_dist = (Math.random() * 1000 + 1).toFixed(1);
    const st_spectype = spectypes[Math.floor(Math.random() * spectypes.length)];
    const st_mass = (Math.random() * 2 + 0.08).toFixed(3);
    const st_rad = (Math.random() * 2 + 0.1).toFixed(3);
    const st_teff = Math.floor(Math.random() * 7000 + 2500);
    
    additional.push({
      id: name,
      pl_name: name,
      pl_discmethod: discmethod,
      pl_facility: facility,
      pl_disc: disc_year,
      pl_masse: masse !== null ? parseFloat(masse) : null,
      pl_rade: rade !== null ? parseFloat(rade) : null,
      pl_orbper: parseFloat(orbper),
      pl_orbsmax: parseFloat(orbsmax),
      pl_orbeccen: parseFloat(orbeccen),
      pl_eqt: eqt,
      st_dist: parseFloat(st_dist),
      st_spectype: st_spectype,
      st_mass: parseFloat(st_mass),
      st_rad: parseFloat(st_rad),
      st_teff: st_teff,
      rowupdate: `2023-${Math.floor(Math.random() * 12) + 1}-${Math.floor(Math.random() * 28) + 1}`,
      nasa_url: `https://exoplanetarchive.ipac.caltech.edu/overview/${encodeURIComponent(name)}`
    });
  }
  
  return additional;
};

